/* ---------- 순수 로직 (node 검증용) ---------- */
/* d6 결과 → 등급. 1=대실패, 6=대성공, dc 이상=성공, 그 외=실패 (1은 항상 대실패) */
function gmTier(roll, dc){
  if (roll === 1) return { tier: "대실패", cls: "fumble" };
  if (roll === 6) return { tier: "대성공", cls: "crit" };
  if (roll >= dc) return { tier: "성공", cls: "ok" };
  return { tier: "실패", cls: "fail" };
}

