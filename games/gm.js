let gm = { last: {}, tumble: 0 };

/* 방금 뽑은 것 바로 재출현 안 되게 (연속 중복 회피) */
function gmPick(table, key){
  if (table.length < 2) return table[0];
  let i, tries = 0;
  do { i = Math.floor(Math.random() * table.length); } while (table[i] === gm.last[key] && ++tries < 8);
  gm.last[key] = table[i];
  return table[i];
}

function gmReset(){
  clearInterval(gm.tumble);
  gm = { last: {}, tumble: 0 };
  $("gm-intro").style.display = "";
  $("gm-play").style.display = "none";
  $("gm-diff").style.display = "none";
  $("gm-dice").style.display = "none";
}

function gmShowCard(kind, text){
  $("gm-diff").style.display = "none";
  $("gm-dice").style.display = "none";
  $("gm-kind").textContent = kind;
  $("gm-card-txt").textContent = text;   /* textContent → 정적 데이터라도 안전하게 */
  haptic(10);
}

$("gm-begin").addEventListener("click", () => {
  $("gm-intro").style.display = "none";
  $("gm-play").style.display = "";
  gmShowCard("여정의 시작", gmPick(GM_OPEN, "open"));
});
$("gm-restart").addEventListener("click", gmReset);

/* 액션 버튼 (data-act) */
$("gm-actions").addEventListener("click", (e) => {
  const b = e.target.closest("button"); if (!b) return;
  const act = b.dataset.act;
  if (act === "scene") gmShowCard("다음 장면", gmPick(GM_SCENE, "scene"));
  else if (act === "twist") gmShowCard("꼬임", gmPick(GM_TWIST, "twist"));
  else if (act === "npc") gmShowCard("등장인물", gmPick(GM_NPC, "npc"));
  else if (act === "end") gmShowCard("결말", gmPick(GM_END, "end"));
  else if (act === "roll"){
    $("gm-dice").style.display = "none";
    $("gm-diff").style.display = ($("gm-diff").style.display === "none" ? "flex" : "none");
  }
});

/* 난이도 선택 → 굴림 */
$("gm-diff").addEventListener("click", (e) => {
  const b = e.target.closest("button"); if (!b) return;
  gmRoll(+b.dataset.dc, b.textContent);
});

function gmRoll(dc, label){
  $("gm-diff").style.display = "none";
  $("gm-dice").style.display = "";
  const face = $("gm-die").firstElementChild; /* px-sprite die1~die6 */
  $("gm-roll-res").innerHTML = "";
  clearInterval(gm.tumble);
  haptic(15);
  let n = 0;
  gm.tumble = setInterval(() => {
    face.setAttribute("name", "die" + (1 + Math.floor(Math.random() * 6)));
    if (++n >= 8){
      clearInterval(gm.tumble);
      const roll = 1 + Math.floor(Math.random() * 6);
      face.setAttribute("name", "die" + roll);
      const t = gmTier(roll, dc);
      const color = t.cls === "crit" ? "var(--steppe)" : t.cls === "fumble" ? "var(--danger)" : t.cls === "ok" ? "var(--milk)" : "var(--dim)";
      $("gm-roll-res").innerHTML =
        '<div class="lbl">' + escHtml(label) + ' · 주사위 ' + roll + '</div>' +
        '<div class="val" style="color:' + color + '">' + t.tier + '</div>' +
        '<div class="gm-txt">' + escHtml(gmPick(GM_ROLL[t.cls], "r_" + t.cls)) + '</div>';
      haptic(roll === 6 ? [30, 40, 30] : roll === 1 ? [60] : 20);
    }
  }, 55);
}
