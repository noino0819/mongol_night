function erOpen(id){
  const o = erObj(id);
  haptic(8); erSnd("open");
  er.panel = id;
  /* 조사 1회 효과: 게이트 안 걸린 examine 오브젝트의 sets/give (lock 없는 것만) */
  if (!er.st.seen[id] && !(o.need && !er.st.flags[o.need])){
    er.st.seen[id] = true;
    if (!o.lock){
      if (o.sets) er.st.flags[o.sets] = true;
      if (o.give) erGain(o.give);
      if (!o.give && !o.sets) { /* 순수 단서 */ }
      if (o.give || o.sets){ erSave(); erRender(); return; }
    }
    erSave();
    erRender();     /* lit 등 플래그로 다른 타일 표시 갱신 */
    return;
  }
  erRenderPanel();
}

/* 8비트 칩튠 효과음 (WebAudio, 오프라인·파일 0). 클릭 제스처 안에서만 호출돼 자동재생 정책 통과.
   ponytail: 전역 음소거 토글은 아직 없음 — 필요해지면 prefs에 snd 추가 */
let erAC = null;
function erSnd(type){
  try {
    erAC = erAC || new (window.AudioContext || window.webkitAudioContext)();
    if (erAC.state === "suspended") erAC.resume();
    const seqs = { solve: [[660, 0], [988, 0.08]], gain: [[880, 0]], combine: [[523, 0], [784, 0.07]],
      wrong: [[165, 0]], tick: [[520, 0]], open: [[392, 0]], knock: [[196, 0]], nope: [[330, 0], [247, 0.09]],
      clear: [[659, 0], [988, 0.1]], win: [[523, 0], [659, 0.11], [784, 0.22], [1047, 0.34]],
      fail: [[392, 0], [311, 0.13], [233, 0.28]] };
    const soft = type === "tick" || type === "open" || type === "knock" || type === "nope";
    const t0 = erAC.currentTime;
    (seqs[type] || seqs.tick).forEach(([f, d]) => {
      const osc = erAC.createOscillator(), g = erAC.createGain(), s = t0 + d;
      osc.type = (type === "wrong" || type === "fail" || type === "knock") ? "sawtooth" : "square";
      osc.frequency.value = f;
      g.gain.setValueAtTime(0.0001, s);
      g.gain.exponentialRampToValueAtTime(soft ? 0.06 : 0.11, s + 0.01);
      g.gain.exponentialRampToValueAtTime(0.0001, s + (type === "tick" || type === "knock" ? 0.08 : 0.15));
      osc.connect(g); g.connect(erAC.destination);
      osc.start(s); osc.stop(s + 0.4);
    });
  } catch (e) { /* 오디오 미지원 무시 */ }
}

function erGain(itemId, silent){
  if (!er.st.inv.includes(itemId)){ er.st.inv.push(itemId); er.justGained = itemId; if (!silent){ erSnd("gain"); } }   /* 단서·아이템 획득 */
}

function erTryCode(){
  const o = erObj(er.panel);
  const inp = $("er-code");
  if (!inp) return;
  if (!erMatch(inp.value, o.lock.ans)){
    erWrong("🔒 잠긴 채다. 단서를 다시 살펴봐.");
    inp.select();
    return;
  }
  erSolve(o);
}

function erUseItem(){
  const o = erObj(er.panel);
  if (!er.st.inv.includes(o.lock.item)) return;
  erSolve(o);
}

function erSolve(o){
  haptic(30); erSnd("solve");   /* 자물쇠 열림 */
  er.st.solved[o.id] = true;
  if (o.lock.sets) er.st.flags[o.lock.sets] = true;
  if (o.lock.give) erGain(o.lock.give);
  erSave();
  if (o.final){ erActClear(); return; }
  /* 해제 결과 문구를 패널에 보여주고 닫기 유도 */
  er.panel = o.id;
  erRender();
  const p = $("er-panel");
  p.innerHTML = '<div class="ta-box er-flash"><div class="ta-port"><px-sprite name="' + o.spr + '" scale="4"></px-sprite></div>' +
    '<div class="ta-body"><div class="ta-nm">🔓 ' + escHtml(o.nm) + '</div><div class="ta-text">' + escHtml(o.lock.open) + "</div></div></div>" +
    '<button class="btn mt" id="er-close">계속</button>';
  p.style.display = "";
  $("er-close").addEventListener("click", () => { er.panel = null; erRenderPanel(); });
  pwaToast("🔓 " + o.nm + " 해제!");
}

function erWrong(msg){
  const box = $("er-msg");
  if (box) box.textContent = msg;
  haptic([40, 40, 40]); erSnd("wrong");   /* 오답·틀린 코드 */
  const bx = $("er-panel").querySelector(".ta-box");
  if (bx){ bx.classList.remove("er-shake"); void bx.offsetWidth; bx.classList.add("er-shake"); }
  if (er.st.mode === "hard"){
    er.st.hearts--;
    er.st.tLeft = Math.max(0, er.st.tLeft - 30);   /* 오답 -30초 */
    erUpdateHud();
    erSave();
    if (er.st.hearts <= 0){ erFail("💔 하트를 다 잃었다…"); return; }
  }
}

function erHint(){
  const o = erObj(er.panel);
  const step = er.hintStep[o.id] || 0;
  if (step >= o.lock.hints.length){ $("er-hintout").textContent = "💡 이 자물쇠 힌트는 여기까지야."; $("er-hintout").style.display = ""; return; }
  $("er-hintout").textContent = "💡 " + o.lock.hints[step];
  $("er-hintout").style.display = "";
  er.hintStep[o.id] = step + 1;
  er.st.hintsTotal = (er.st.hintsTotal || 0) + 1;
  if (er.st.mode === "hard"){ er.st.tLeft = Math.max(0, er.st.tLeft - 15); erUpdateHud(); }  /* 힌트 -15초 */
  erSave();
}

function erCombine(){
  const sel = er.sel.slice().sort();
  const combo = erAct().combos.find((c) => c.need.slice().sort().join() === sel.join());
  if (!combo){ erSnd("nope"); pwaToast("이건 서로 안 맞아 — 다른 조합을 찾아봐"); er.sel = []; erRenderInv(); return; }
  /* 재료 소모 → 결과 아이템 */
  er.st.inv = er.st.inv.filter((id) => !combo.need.includes(id));
  erGain(combo.gives, true);
  er.sel = [];
  haptic(30); erSnd("combine");
  erSave();
  erRender();
  pwaToast("🔗 " + combo.text);
}

