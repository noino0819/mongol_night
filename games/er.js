function erTick(){
  if (!er.st) return;
  if (er.st.mode === "hard"){
    er.st.tLeft--;
    if (er.st.tLeft <= 0){ er.st.tLeft = 0; erUpdateHud(); erFail("⏳ 시간 초과! 모래폭풍이 닿았다…"); return; }
  } else {
    er.st.tLeft = (er.st.tLeft || 0) + 1;   /* 소프트: 경과 카운트업 */
  }
  erUpdateHud();
}

